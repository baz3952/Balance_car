#include "iic.h"

//PB6/7
//初始化I2C
void IIC_Init(void)
{
	GPIO_InitTypeDef GPIO_InitStructure;
	RCC_APB2PeriphClockCmd(RCC_APB2Periph_GPIOB,ENABLE);
	
	GPIO_InitStructure.GPIO_Pin = GPIO_Pin_3|GPIO_Pin_4;
	GPIO_InitStructure.GPIO_Mode = GPIO_Mode_Out_PP;
	GPIO_InitStructure.GPIO_Speed = GPIO_Speed_50MHz;
	GPIO_Init(GPIOB,&GPIO_InitStructure);
	IIC_SCL = 1;
	IIC_SDA = 1;
}

//起始信号
void IIC_Start(void)
{
	SDA_OUT();
	IIC_SDA = 1;IIC_SCL = 1;
	delay_us(2);
	IIC_SDA = 0;
	delay_us(2);
	IIC_SCL = 0;
}

//停止信号
void IIC_Stop(void)
{
	SDA_OUT();
	IIC_SCL = 0;
	IIC_SDA = 0;
	delay_us(2);
	IIC_SCL = 1;IIC_SDA = 1;
	delay_us(2);
}

//等待应答信号
//return 1：失败
//			 0：成功
u8 IIC_Wait_Ack(void)
{
	u8 ucErrTime = 0;
	SDA_IN();						//SDA设置为输出
	IIC_SDA = 1;delay_us(2);
	IIC_SCL = 1;delay_us(2);
	while(READ_SDA)
	{
		ucErrTime++;
		if(ucErrTime>250)
		{
			IIC_Stop();
			return 1;
		}
	}
	IIC_SCL = 0;	//时钟输出0
	return 0;
}

//产生ACK应答
void IIC_Ack(void)
{
	IIC_SCL = 0;SDA_OUT();
	IIC_SDA = 0;delay_us(2);
	IIC_SCL = 1;delay_us(2);
	IIC_SCL = 0;
}

//不产生ACK应答
void IIC_NAck(void)
{
	IIC_SCL = 0;
	SDA_OUT();
	IIC_SDA = 1;delay_us(2);
	IIC_SCL = 1;delay_us(2);
	IIC_SCL = 0;
}

//I2C发送一个字节
//return 1:有应答
//			 0:无应答
void IIC_Send_Byte(u8 txd)
{
	u8 t;
	SDA_OUT();
	IIC_SCL = 0;
	for(t = 0;t < 8;t++)
	{
		IIC_SDA = (txd&0x80) >> 7;
		txd <<= 1;
		delay_us(2);
		IIC_SCL = 1;delay_us(2);
		IIC_SCL = 0;delay_us(2);
	}
}

//读一字节，ack=1时，发送ACK,ack=0时，发送nACK
u8 IIC_Read_Byte(u8 ack)
{
	unsigned char i,receive=0;
	SDA_IN();//SDA 设置为输入
	for(i=0;i<8;i++ )
	{
		IIC_SCL=0; delay_us(2);
		IIC_SCL=1;
		receive<<=1;
		if(READ_SDA)receive++;
		delay_us(2);
	}
	if (!ack) IIC_NAck();//发送 nACK
	else IIC_Ack(); //发送 ACK
	return receive;
}
